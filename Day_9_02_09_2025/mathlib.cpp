#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    cout<<"Square Root"<<endl;
    cout<<"square root of 4  :  "<<sqrt(4)<<endl;
    cout<<"square root  of 20  :  "<<sqrt(20)<<endl;
    cout<<"square root  of -49  :  "<<sqrt(-49)<<endl<<endl;
    
    cout<<"Absolute"<<endl;
    cout<<"absolute of 1.9  :  "<<abs(1.9)<<endl;
    cout<<"absolute of -47  :  "<<abs(-47)<<endl;
    cout<<"absolute of 1.7  :  "<<abs(-1.7)<<endl<<endl;

    cout<<"Power"<<endl;

    cout<<"power 2^4  :  "<<pow(2,4)<<endl;
    cout<<"power 3^2  :  "<<pow(3,2)<<endl;
    cout<<"power 5^-3  :  "<<pow(5,-3)<<endl<<endl;

    cout<<"Ceiling"<<endl;

    cout<<"ceiling of 8.1  :  "<<ceil(8.1)<<endl;
    cout<<"ceiling of -8.8  :  "<<ceil(-8.8)<<endl;
    cout<<"ceiling of -0.3  :  "<<ceil(-0.3)<<endl<<endl;

    cout<<"Floor"<<endl;

    cout<<"floor of 9.2  :  "<<floor(9.2)<<endl;
    cout<<"floor of 8.2  :  "<<floor(8.2)<<endl;
    cout<<"floor of -8.2  :  "<<floor(-8.2)<<endl<<endl;

    cout<<"Round"<<endl;

    cout<<"rounding of 9.6  :  "<<round(9.6)<<endl;
    cout<<"rounding of 9.5  :  "<<round(9.5)<<endl;
    cout<<"rounding of 9.2  :  "<<round(9.2)<<endl;
    cout<<"rounding of -9.2  :  "<<round(-9.2)<<endl;
    cout<<"rounding of -9.6  :  "<<round(-9.6)<<endl;
    cout<<"rounding of -9.5  :  "<<round(-9.5)<<endl;


}