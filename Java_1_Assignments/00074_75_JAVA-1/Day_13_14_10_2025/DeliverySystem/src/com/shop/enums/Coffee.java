package com.shop.enums;

public enum Coffee {

	small(200,500.00),medium(300,700.00),large(400,900.00);
	
	public int getSize() {
		return size;
	}



	public void setSize(int size) {
		this.size = size;
	}



	public double getPrice() {
		return price;
	}



	public void setPrice(double price) {
		this.price = price;
	}



	private int size;
	private double price;
	
	

	private Coffee() {
	}



	private Coffee(int i, double d) {
		this.size=i;
		this.price=d;
		
	}
}
